"""Export repair GUI state into pipeline-friendly recipe JSON."""

from __future__ import annotations

import json
from dataclasses import asdict
from pathlib import Path

from .models import RepairProject, SCHEMA_VERSION


def export_recipe(path: Path, project: RepairProject) -> None:
    """Write a recipe JSON that can later be consumed by the CLI pipeline."""

    repair = project.active_repair
    for lane in repair.lanes:
        lane.store_selected_clip_item()
    payload = {
        "schema_version": SCHEMA_VERSION,
        "kind": "robotech_audio_repair_recipe",
        "episode": project.episode,
        "title": project.title,
        "sample_rate": project.sample_rate,
        "channels": project.channels,
        "sources": {
            "main_track": project.main_track,
            "bed_track": project.bed_track,
            "bed_preview_mode": project.bed_preview_mode,
            "video_track": project.video_track,
            "reference_tracks": project.reference_tracks,
        },
        "repair_id": repair.repair_id,
        "marker_seconds": repair.marker_seconds,
        "work_start_seconds": repair.work_start_seconds,
        "work_end_seconds": repair.work_end_seconds,
        "cut_start_seconds": repair.cut_start_seconds,
        "cut_end_seconds": repair.cut_end_seconds,
        "work_window_seconds": repair.work_window_seconds,
        "snap_ms": repair.snap_ms,
        "selected_gain_db": repair.selected_gain_db,
        "edge_source_seconds": repair.edge_source_seconds,
        "edge": asdict(repair.edge),
        "lanes": [asdict(lane) for lane in repair.lanes if lane.path],
        "actions": [asdict(action) for action in repair.actions],
        "render_targets": {
            "work_clip_mix": "Render visible work-window preview plus all unmuted lanes.",
            "bed_plus_work_clip_mix": "Render visible bed work-window preview plus visible work mix.",
            "full_main_patched": "Replace the full main track work-window span with the visible work mix.",
            "pipeline_patch": "Use actions and lanes to patch the extracted Spanish dialogue before final 5.1 mix.",
        },
        "notes": "Generated by the repair GUI. Deterministic work actions and unmuted lanes can be imported by episode-final-build with --repair-recipe.",
    }
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
